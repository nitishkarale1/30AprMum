package Control_Statements;

public class sample24_else_if 
{
	public static void main(String[] args) 
	{
		int x=79;
		int y=79;
		
		if (x<y)
		{
			System.out.println("x is less than y");
		}
		
		else if (x==y) 
		{
			System.out.println("x is equal to y");
		}
		else if (x>y)
		{
			System.out.println("x is greater than y");
		}
		
	}

}
