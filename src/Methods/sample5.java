package Methods;

public class sample5 

{
	
	//static regular method from same class
	
		public static void main(String[] args)   //pre-defined method
		
		{
			System.out.println("important social media apps");
			System.out.println("1) facebook");
			
			E();  //method call-->methodname();
			F();
			
		}
		
		//static regular method
		public static void E()   //user defined method
		
		{
			System.out.println("2)Instagram");
			System.out.println("3)Twitter");
		}
		
		public static void F()    //user defined method
		
		{
			System.out.println("4)Whatsapp");
			System.out.println("5)Telegram");
		}

}
