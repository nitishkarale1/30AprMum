package Methods;

public class sample3 

{
	//static regular method call from same class
	
			public static void main(String[] args) 
			
			{
			   
				System.out.println("12+25=37");
				
				A();   ///method call-->methodname();
			    A1();
			    A2();
			}
			
			public static void A()    //static regular method in same class
			{
				System.out.println("55-20=35");
			}
			
			public static void A1()
			{
				System.out.println("15*8=120");
			}
			
			public static void A2()
			{
				System.out.println("220+280=500");
			}


}
