package For_loop;

public class sample18_forLoop_printNumMultipleOf_7_From_7_to_90 
{
	public static void main(String[] args) 
	{
		//start num:7
		//end num:90
		//incr/decr:incr by7
		      //  7  91<=90    91
	    for(int m=7; m<=90; m=m+7)
		{
			System.out.println(m);    // 7 14 21 28 35 42 49 56 63 70 77 84
		}
		
	}

}
