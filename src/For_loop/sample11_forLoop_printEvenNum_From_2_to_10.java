package For_loop;

public class sample11_forLoop_printEvenNum_From_2_to_10 
{
	public static void main(String[] args) 
	{
		//start num:2
		//end num:10
		//incr/decr:incr by 2
		
		        //2  12<=10 12
	for (int A=2; A<=10; A=A+2)
	   {
		   System.out.println(A);
	   }
	}
	

}
