package For_loop;

public class sample1_forLoop1_print_num_from_1_to_5 
{
	public static void main(String[] args) 
	{
		//print from 1 to 5
		//start num: 1
		//end num: 5
		//incr/decr: incr by 1
		
		// 1 6<=5 6
		for(int i=1; i<=5;  i++)
		{
			System.out.println(i);   //1 2 3 4 5
		}
		
	}
	
		
	

}
