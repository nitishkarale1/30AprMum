package For_loop;

public class sample5_forLoop1_print_num_from_51_to_60 
{
	public static void main(String[] args) 
	{
		//start num:51
		//end num:60
		//incr/decr:incr by1
		
		       //    51  k<=61  61
		for (int k=51; k<=60; k++)
		{
			System.out.println(k);
		}
	
		
	}

}
