package Variable;

public class sample6 

{
	
	public static void main(String[] args)
	{
		
		       
		        //variable declaration (allocating/reserving memory)
				String StudentName;    //datatype variableName
				String StudentDept;
				int StudentRollNum;
				char StudentGrade;
				float StudentPer;
				
		       
				//variable Initialization (Assigning Value)
				StudentName = "Arjun";     //Variable value
				StudentDept = "Electrical";
				StudentRollNum = 101;
				StudentGrade = 'A';
				StudentPer = 65.5f;
				
		        //Variable Usage
		        System.out.println("Student Name: "+ StudentName);
				System.out.println("Student Dept: "+ StudentDept);
				System.out.println("StudentRollNum: "+ StudentRollNum);
				System.out.println("StudentGrade: "+ StudentGrade);
				System.out.println("StudentPercentage: "+ StudentPer+"%");
	}
	

}
